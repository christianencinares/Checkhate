# TRANSLATE WITH TARGET LANGUAGE
def detect_and_translate(text,target_lang):
    translator = google_translator(proxies={'http':'122.2.28.114'})  
    translation = translator.translate(text,lang_tgt=target_lang)
    return translation

# AUTO TRANSLATE
def translate(text):
    translator = Translator()
